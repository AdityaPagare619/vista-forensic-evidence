class TelemetryCollector:
    """
    Collects and aggregates device telemetry.

    Supports:
      - Multi-device telemetry ingestion
      - Time-windowed aggregation (min, max, mean)
      - Telemetry history for evidence chain
    """

    def __init__(self, aggregation_window_s: float = 60.0):
        self._window_s = aggregation_window_s
        self._telemetry: Dict[str, List[TelemetryEntry]] = {}  # device_id -> entries
        self._sequence_counters: Dict[str, int] = {}

    def ingest(self, device_id: str, telemetry_type: TelemetryType,
               value: Any, timestamp_s: Optional[float] = None) -> TelemetryEntry:
        """
        Ingest a telemetry data point from a device.

        Args:
            device_id: Source device ID
            telemetry_type: Type of telemetry
            value: Telemetry value
            timestamp_s: Timestamp (current time if None)

        Returns:
            TelemetryEntry with assigned sequence number
        """
        if timestamp_s is None:
            timestamp_s = time.time()

        # Auto-increment sequence
        key = f"{device_id}:{telemetry_type.value}"
        seq = self._sequence_counters.get(key, 0)
        self._sequence_counters[key] = seq + 1

        entry = TelemetryEntry(
            device_id=device_id,
            telemetry_type=telemetry_type,
            value=value,
            timestamp_s=timestamp_s,
            sequence=seq,
        )

        if device_id not in self._telemetry:
            self._telemetry[device_id] = []
        self._telemetry[device_id].append(entry)

        return entry

    def get_entries(self, device_id: str,
                    telemetry_type: Optional[TelemetryType] = None,
                    since_s: Optional[float] = None) -> List[TelemetryEntry]:
        """Get telemetry entries for a device."""
        entries = self._telemetry.get(device_id, [])

        if telemetry_type is not None:
            entries = [e for e in entries if e.telemetry_type == telemetry_type]

        if since_s is not None:
            entries = [e for e in entries if e.timestamp_s >= since_s]

        return entries

    def aggregate(self, device_id: str,
                  telemetry_type: TelemetryType,
                  window_s: Optional[float] = None) -> Optional[TelemetryAggregate]:
        """
        Compute aggregated telemetry over a time window.

        Returns:
            TelemetryAggregate with min/max/mean/count
        """
        window = window_s or self._window_s
        now = time.time()
        since = now - window

        entries = self.get_entries(device_id, telemetry_type, since_s=since)
        if not entries:
            return None

        values = [float(e.value) for e in entries if isinstance(e.value, (int, float))]
        if not values:
            return None

        return TelemetryAggregate(
            device_id=device_id,
            telemetry_type=telemetry_type,
            count=len(values),
            min_value=min(values),
            max_value=max(values),
            mean_value=sum(values) / len(values),
            last_value=values[-1],
            window_start_s=since,
            window_end_s=now,
        )

    def get_all_device_ids(self) -> Set[str]:
        """Get all device IDs that have sent telemetry."""
        return set(self._telemetry.keys())

    def get_latest_telemetry(self, device_id: str) -> Dict[TelemetryType, TelemetryEntry]:
        """Get the latest value for each telemetry type from a device."""
        entries = self._telemetry.get(device_id, [])
        latest = {}
        for entry in reversed(entries):
            if entry.telemetry_type not in latest:
                latest[entry.telemetry_type] = entry
        return latest

    def get_total_entries(self) -> int:
        """Get total telemetry entries across all devices."""
        return sum(len(entries) for entries in self._telemetry.values())

    def to_dict(self) -> Dict[str, Any]:
        return {
            "total_entries": self.get_total_entries(),
            "device_count": len(self._telemetry),
            "devices": {
                did: len(entries)
                for did, entries in self._telemetry.items()
            },
        }


