@dataclass
class DeviceIdentity:
    """
    TPM-like device identity with hardware-bound keys.

    In production, this would interface with a TPM 2.0 chip or
    secure element. For simulation, we use deterministic generation
    from a hardware seed.
    """
    device_id: str
    tpm_pubkey: str
    tpm_cert: str
    hardware_serial: str
    manufacturer: str = "VISTA"
    model: str = "VISTA-2.0-HIL"
    firmware_version: str = "2.0.0"
    registration_time: float = 0.0
    last_seen: float = 0.0
    status: DeviceStatus = DeviceStatus.ONLINE

    def to_dict(self) -> Dict[str, Any]:
        return {
            "device_id": self.device_id,
            "tpm_pubkey": self.tpm_pubkey,
            "tpm_cert": self.tpm_cert,
            "hardware_serial": self.hardware_serial,
            "manufacturer": self.manufacturer,
            "model": self.model,
            "firmware_version": self.firmware_version,
            "registration_time": self.registration_time,
            "last_seen": self.last_seen,
            "status": self.status.value,
        }


def generate_tpm_identity(hardware_seed: str) -> DeviceIdentity:
    """
    Generate a TPM-like device identity from a hardware seed.

    In production:
      - hardware_seed comes from TPM芯片 unique serial
      - tpm_pubkey is generated by TPM's RSA/ECC keygen
      - tpm_cert is signed by manufacturer CA

    For simulation:
      - Deterministic generation from seed for reproducibility
    """
    seed_hash = hashlib.sha256(hardware_seed.encode()).hexdigest()

    device_id = f"VISTA-{seed_hash[:12].upper()}"
    tpm_pubkey = f"tpm-pub-{seed_hash[:32]}"
    tpm_cert = f"tpm-cert-{hashlib.sha3_256(hardware_seed.encode()).hexdigest()[:48]}"
    hardware_serial = seed_hash[:16].upper()

    return DeviceIdentity(
        device_id=device_id,
        tpm_pubkey=tpm_pubkey,
        tpm_cert=tpm_cert,
        hardware_serial=hardware_serial,
        registration_time=time.time(),
        last_seen=time.time(),
    )


