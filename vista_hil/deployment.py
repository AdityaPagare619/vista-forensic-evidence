# Fleet Manager (Top-Level Orchestrator)
# ===================================================================

class FleetManager:
    """
    Top-level fleet management orchestrator.

    Coordinates:
      - DeviceRegistry for identity
      - TelemetryCollector for data
      - OTAUpdateManager for firmware
      - FleetEvidenceChain for integrity
      - FleetHealthMonitor for alerts
    """

    def __init__(self, shared_secret: bytes = b"fleet-evidence-secret-32b!"):
        self.registry = DeviceRegistry()
        self.telemetry = TelemetryCollector()
        self.ota = OTAUpdateManager()
        self.evidence = FleetEvidenceChain(shared_secret)
        self.health = FleetHealthMonitor()

    def register_device(self, hardware_seed: str) -> DeviceIdentity:
        """Register a device and create evidence record."""
        identity = self.registry.register_device(hardware_seed)
        self.evidence.append_record(
            identity.device_id, "registration",
            {"hardware_serial": identity.hardware_serial,
             "model": identity.model}
        )
        return identity

    def process_heartbeat(self, device_id: str,
                          telemetry_data: Optional[Dict[TelemetryType, Any]] = None):
        """Process a device heartbeat with optional telemetry."""
        self.registry.heartbeat(device_id)

        # Record heartbeat evidence
        self.evidence.append_record(device_id, "heartbeat", {})

        # Ingest telemetry if provided
        if telemetry_data:
            for ttype, value in telemetry_data.items():
                self.telemetry.ingest(device_id, ttype, value)

        # Health checks
        device = self.registry.get_device(device_id)
        if device:
            self.health.check_heartbeat(device)

    def record_crash_event(self, device_id: str,
                           crash_data: Dict[str, Any]):
        """Record a crash event from a device."""
        self.evidence.append_record(device_id, "crash", crash_data)
        self.health.check_crash_event(device_id, crash_data)

    def get_fleet_status(self) -> Dict[str, Any]:
        """Get comprehensive fleet status."""
        return {
            "registry": self.registry.to_dict(),
            "telemetry": self.telemetry.to_dict(),
            "ota": self.ota.to_dict(),
            "evidence": {
                "device_count": len(self.evidence.get_device_ids()),
                "total_records": self.evidence.get_total_records(),
            },
            "health": self.health.get_fleet_summary(),
        }


