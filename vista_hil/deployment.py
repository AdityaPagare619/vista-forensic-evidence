class OTAUpdateManager:
    """
    Manages over-the-air firmware updates for the fleet.

    Handles:
      - Package creation and distribution
      - Update scheduling and tracking
      - Rollback management
      - Update verification
    """

    def __init__(self):
        self._packages: Dict[str, OTAPackage] = {}
        self._update_records: Dict[str, List[OTAUpdateRecord]] = {}  # device_id -> records
        self._pending_updates: Dict[str, str] = {}  # device_id -> package_id

    def create_package(self, version: str, description: str,
                       firmware_data: bytes,
                       target_fw_versions: Optional[List[str]] = None,
                       metadata: Optional[Dict[str, Any]] = None) -> OTAPackage:
        """
        Create a new OTA update package.

        Args:
            version: Target firmware version
            description: Update description
            firmware_data: Binary firmware payload
            target_fw_versions: Compatible current versions
            metadata: Additional package metadata

        Returns:
            OTAPackage with checksum
        """
        checksum = hashlib.sha256(firmware_data).hexdigest()
        package_id = f"ota-{version}-{checksum[:8]}"

        package = OTAPackage(
            package_id=package_id,
            version=version,
            description=description,
            checksum_sha256=checksum,
            size_bytes=len(firmware_data),
            created_at=time.time(),
            target_fw_versions=target_fw_versions or [],
            metadata=metadata or {},
        )

        self._packages[package_id] = package
        return package

    def schedule_update(self, device_id: str, package_id: str) -> bool:
        """
        Schedule an OTA update for a device.

        Returns True if scheduled, False if package not found.
        """
        if package_id not in self._packages:
            return False

        self._pending_updates[device_id] = package_id

        record = OTAUpdateRecord(
            device_id=device_id,
            package_id=package_id,
            status=OTAPackageStatus.PENDING,
            started_at=time.time(),
        )

        if device_id not in self._update_records:
            self._update_records[device_id] = []
        self._update_records[device_id].append(record)

        return True

    def simulate_update(self, device_id: str, success: bool = True,
                        from_version: str = "2.0.0",
                        to_version: str = "2.1.0") -> OTAUpdateRecord:
        """
        Simulate an OTA update execution (for HIL testing).

        Args:
            device_id: Target device
            success: Whether update succeeds
            from_version: Current firmware version
            to_version: Target firmware version

        Returns:
            OTAUpdateRecord with final status
        """
        records = self._update_records.get(device_id, [])
        if not records:
            # Create a record if none exists
            record = OTAUpdateRecord(
                device_id=device_id,
                package_id="simulated",
                status=OTAPackageStatus.PENDING,
                started_at=time.time(),
                from_version=from_version,
                to_version=to_version,
            )
            records.append(record)
            self._update_records[device_id] = records

        record = records[-1]
        record.from_version = from_version
        record.to_version = to_version

        if success:
            # Simulate progress through states
            record.status = OTAPackageStatus.DOWNLOADING
            record.status = OTAPackageStatus.VERIFYING
            record.status = OTAPackageStatus.INSTALLING
            record.status = OTAPackageStatus.COMPLETE
            record.completed_at = time.time()
        else:
            record.status = OTAPackageStatus.FAILED
            record.error_message = "Simulated update failure"
            record.completed_at = time.time()

        # Clean up pending
        self._pending_updates.pop(device_id, None)

        return record

    def get_update_history(self, device_id: str) -> List[OTAUpdateRecord]:
        """Get update history for a device."""
        return self._update_records.get(device_id, [])

    def get_pending_updates(self) -> Dict[str, str]:
        """Get all pending updates (device_id -> package_id)."""
        return dict(self._pending_updates)

    def get_package(self, package_id: str) -> Optional[OTAPackage]:
        """Get a package by ID."""
        return self._packages.get(package_id)

    def verify_package(self, package_id: str, firmware_data: bytes) -> bool:
        """Verify firmware data matches package checksum."""
        package = self._packages.get(package_id)
        if package is None:
            return False
        actual_checksum = hashlib.sha256(firmware_data).hexdigest()
        return package.checksum_sha256 == actual_checksum

    def to_dict(self) -> Dict[str, Any]:
        return {
            "package_count": len(self._packages),
            "pending_count": len(self._pending_updates),
            "packages": {pid: {
                "version": p.version,
                "size_bytes": p.size_bytes,
                "created_at": p.created_at,
            } for pid, p in self._packages.items()},
            "update_stats": {
                did: len(records)
                for did, records in self._update_records.items()
            },
        }


