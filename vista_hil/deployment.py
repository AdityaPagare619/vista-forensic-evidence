class FleetEvidenceChain:
    """
    Cryptographic evidence chain from device to cloud.

    Implements a hash-chained ledger where each record's hash includes
    the previous record's hash, making tampering detectable.

    Security properties:
      - Append-only (cannot modify without breaking chain)
      - Dual hashing (SHA-256 + SHA-3) for defense-in-depth
      - Per-device sequence numbers for gap detection
    """

    def __init__(self, shared_secret: bytes = b"fleet-evidence-secret-32b!"):
        if len(shared_secret) < MIN_TPM_KEY_LENGTH:
            raise ValueError(f"Secret must be >= {MIN_TPM_KEY_LENGTH} bytes")
        self._secret = shared_secret
        self._chains: Dict[str, List[FleetEvidenceRecord]] = {}  # device_id -> records
        self._global_sequence = 0

    def _compute_hash(self, data: bytes, algorithm: str = "sha256") -> str:
        """Compute hash."""
        if algorithm == "sha3_256":
            return hashlib.sha3_256(data).hexdigest()
        return hashlib.sha256(data).hexdigest()

    def _compute_hmac(self, data: bytes) -> str:
        """Compute HMAC."""
        return hashlib.pbkdf2_hmac("sha256", self._secret, data, 100000).hex()

    def append_record(self, device_id: str, event_type: str,
                      payload: Dict[str, Any]) -> FleetEvidenceRecord:
        """
        Append a new evidence record to the chain.

        Args:
            device_id: Source device
            event_type: Event type (crash, heartbeat, etc.)
            payload: Evidence payload

        Returns:
            FleetEvidenceRecord with hash chain
        """
        if device_id not in self._chains:
            self._chains[device_id] = []

        chain = self._chains[device_id]
        sequence = len(chain)
        previous_hash = chain[-1].sha256_hash if chain else "0" * 64

        # Build record
        timestamp = time.time()
        record_id = f"fe-{device_id}-{sequence}-{int(timestamp * 1000)}"

        # Serialise for hashing
        record_data = {
            "record_id": record_id,
            "device_id": device_id,
            "event_type": event_type,
            "payload": payload,
            "timestamp_s": timestamp,
            "sequence": sequence,
            "previous_hash": previous_hash,
        }

        data_bytes = json.dumps(record_data, sort_keys=True).encode("utf-8")
        sha256_hash = self._compute_hash(data_bytes, "sha256")
        sha3_hash = self._compute_hash(data_bytes, "sha3_256")

        record = FleetEvidenceRecord(
            record_id=record_id,
            device_id=device_id,
            event_type=event_type,
            payload=payload,
            timestamp_s=timestamp,
            sha256_hash=sha256_hash,
            sha3_hash=sha3_hash,
            sequence=sequence,
            previous_hash=previous_hash,
        )

        chain.append(record)
        self._global_sequence += 1

        return record

    def verify_chain(self, device_id: str) -> Dict[str, Any]:
        """
        Verify the integrity of a device's evidence chain.

        Checks:
          1. Each record's hash matches re-computation
          2. Previous hash links are correct
          3. Sequence numbers are monotonic
          4. Dual hash consistency

        Returns:
            Dict with "valid", "checks", "errors"
        """
        chain = self._chains.get(device_id, [])
        errors = []
        checks = {}

        if not chain:
            return {"valid": True, "checks": {}, "errors": [], "record_count": 0}

        # Check each record
        for i, record in enumerate(chain):
            # Recompute hash
            record_data = {
                "record_id": record.record_id,
                "device_id": record.device_id,
                "event_type": record.event_type,
                "payload": record.payload,
                "timestamp_s": record.timestamp_s,
                "sequence": record.sequence,
                "previous_hash": record.previous_hash,
            }
            data_bytes = json.dumps(record_data, sort_keys=True).encode("utf-8")
            expected_sha256 = self._compute_hash(data_bytes, "sha256")
            expected_sha3 = self._compute_hash(data_bytes, "sha3_256")

            if record.sha256_hash != expected_sha256:
                errors.append(f"Record {i}: SHA-256 mismatch")
                checks[f"record_{i}_sha256"] = False
            else:
                checks[f"record_{i}_sha256"] = True

            if record.sha3_hash != expected_sha3:
                errors.append(f"Record {i}: SHA-3 mismatch")
                checks[f"record_{i}_sha3"] = False
            else:
                checks[f"record_{i}_sha3"] = True

            # Check previous hash link
            if i > 0:
                expected_prev = chain[i - 1].sha256_hash
                if record.previous_hash != expected_prev:
                    errors.append(f"Record {i}: Broken chain link")
                    checks[f"record_{i}_chain"] = False
                else:
                    checks[f"record_{i}_chain"] = True

            # Check sequence
            if record.sequence != i:
                errors.append(f"Record {i}: Sequence mismatch (expected {i}, got {record.sequence})")
                checks[f"record_{i}_sequence"] = False
            else:
                checks[f"record_{i}_sequence"] = True

        valid = len(errors) == 0

        return {
            "valid": valid,
            "checks": checks,
            "errors": errors,
            "record_count": len(chain),
        }

    def get_chain(self, device_id: str) -> List[FleetEvidenceRecord]:
        """Get evidence chain for a device."""
        return list(self._chains.get(device_id, []))

    def get_device_ids(self) -> Set[str]:
        """Get all device IDs with evidence chains."""
        return set(self._chains.keys())

    def get_total_records(self) -> int:
        """Get total records across all devices."""
        return sum(len(chain) for chain in self._chains.values())


