class FleetHealthMonitor:
    """
    Fleet-wide health monitoring and alerting.

    Monitors:
      - Device heartbeat staleness
      - Telemetry anomalies
      - Update status
      - Crash event frequency
      - Storage and battery levels
    """

    def __init__(self,
                 heartbeat_timeout_s: float = 300.0,
                 battery_warning_pct: float = 20.0,
                 storage_warning_gb: float = 5.0,
                 temp_warning_c: float = 70.0):
        self._heartbeat_timeout_s = heartbeat_timeout_s
        self._battery_warning_pct = battery_warning_pct
        self._storage_warning_gb = storage_warning_gb
        self._temp_warning_c = temp_warning_c
        self._alerts: List[FleetAlert] = []
        self._alert_counter = 0

    @property
    def alert_count(self) -> int:
        return len(self._alerts)

    @property
    def unacknowledged_count(self) -> int:
        return sum(1 for a in self._alerts if not a.acknowledged)

    def _create_alert(self, device_id: str, severity: AlertSeverity,
                      message: str, metadata: Optional[Dict] = None) -> FleetAlert:
        """Create and store an alert."""
        self._alert_counter += 1
        alert = FleetAlert(
            alert_id=f"alert-{self._alert_counter:06d}",
            device_id=device_id,
            severity=severity,
            message=message,
            timestamp_s=time.time(),
            metadata=metadata or {},
        )
        self._alerts.append(alert)
        return alert

    def check_heartbeat(self, device: DeviceIdentity) -> Optional[FleetAlert]:
        """Check if a device's heartbeat is stale."""
        age = time.time() - device.last_seen
        if age > self._heartbeat_timeout_s:
            return self._create_alert(
                device.device_id,
                AlertSeverity.WARNING,
                f"Heartbeat stale: {age:.0f}s (timeout: {self._heartbeat_timeout_s}s)",
                {"age_s": age, "timeout_s": self._heartbeat_timeout_s},
            )
        return None

    def check_battery(self, device_id: str,
                      battery_pct: float) -> Optional[FleetAlert]:
        """Check battery level."""
        if battery_pct < self._battery_warning_pct:
            severity = (AlertSeverity.CRITICAL if battery_pct < 10
                       else AlertSeverity.WARNING)
            return self._create_alert(
                device_id, severity,
                f"Low battery: {battery_pct:.1f}%",
                {"battery_pct": battery_pct},
            )
        return None

    def check_storage(self, device_id: str,
                      free_gb: float) -> Optional[FleetAlert]:
        """Check storage level."""
        if free_gb < self._storage_warning_gb:
            severity = (AlertSeverity.CRITICAL if free_gb < 1.0
                       else AlertSeverity.WARNING)
            return self._create_alert(
                device_id, severity,
                f"Low storage: {free_gb:.1f} GB free",
                {"free_gb": free_gb},
            )
        return None

    def check_cpu_temp(self, device_id: str,
                       temp_c: float) -> Optional[FleetAlert]:
        """Check CPU temperature."""
        if temp_c > self._temp_warning_c:
            severity = (AlertSeverity.CRITICAL if temp_c > 85
                       else AlertSeverity.WARNING)
            return self._create_alert(
                device_id, severity,
                f"High CPU temperature: {temp_c:.1f}°C",
                {"temp_c": temp_c},
            )
        return None

    def check_crash_event(self, device_id: str,
                          crash_data: Dict[str, Any]) -> FleetAlert:
        """Generate alert for a crash event."""
        severity = AlertSeverity.INFO
        delta_v = crash_data.get("delta_v_kmh", 0)
        if delta_v > 80:
            severity = AlertSeverity.CRITICAL
        elif delta_v > 40:
            severity = AlertSeverity.WARNING

        return self._create_alert(
            device_id, severity,
            f"Crash event: ΔV={delta_v:.1f} km/h",
            crash_data,
        )

    def check_update_status(self, device_id: str,
                            record: OTAUpdateRecord) -> Optional[FleetAlert]:
        """Check OTA update status."""
        if record.status == OTAPackageStatus.FAILED:
            return self._create_alert(
                device_id, AlertSeverity.ERROR,
                f"OTA update failed: {record.error_message}",
                {"package_id": record.package_id},
            )
        return None

    def acknowledge_alert(self, alert_id: str) -> bool:
        """Acknowledge an alert."""
        for alert in self._alerts:
            if alert.alert_id == alert_id:
                alert.acknowledged = True
                return True
        return False

    def get_alerts(self, device_id: Optional[str] = None,
                   severity: Optional[AlertSeverity] = None,
                   unacknowledged_only: bool = False) -> List[FleetAlert]:
        """Get alerts with optional filters."""
        alerts = self._alerts

        if device_id is not None:
            alerts = [a for a in alerts if a.device_id == device_id]

        if severity is not None:
            alerts = [a for a in alerts if a.severity == severity]

        if unacknowledged_only:
            alerts = [a for a in alerts if not a.acknowledged]

        return alerts

    def get_fleet_summary(self) -> Dict[str, Any]:
        """Get fleet-wide health summary."""
        severity_counts = {}
        for alert in self._alerts:
            sev = alert.severity.value
            severity_counts[sev] = severity_counts.get(sev, 0) + 1

        return {
            "total_alerts": self.alert_count,
            "unacknowledged": self.unacknowledged_count,
            "severity_counts": severity_counts,
        }

    def to_dict(self) -> Dict[str, Any]:
        return {
            **self.get_fleet_summary(),
            "alerts": [a.to_dict() for a in self._alerts[-100:]],
        }


