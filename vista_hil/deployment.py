class DeviceRegistry:
    """
    TPM-like device registration and identity management.

    Handles:
      - Device registration with hardware-bound identity
      - Identity verification via TPM public key
      - Device status tracking
      - Firmware version management

    Security model:
      - Each device has a unique TPM-derived identity
      - Identity is bound to hardware (can't be cloned)
      - Registration requires manufacturer signature (simulated)
    """

    def __init__(self):
        self._devices: Dict[str, DeviceIdentity] = {}
        self._registration_log: List[Dict[str, Any]] = []
        self._tpm_key_index: Dict[str, str] = {}  # pubkey -> device_id

    @property
    def device_count(self) -> int:
        return len(self._devices)

    @property
    def online_count(self) -> int:
        return sum(1 for d in self._devices.values()
                   if d.status == DeviceStatus.ONLINE)

    def register_device(self, hardware_seed: str,
                        manufacturer: str = "VISTA",
                        model: str = "VISTA-2.0-HIL") -> DeviceIdentity:
        """
        Register a new device with TPM-like identity.

        Args:
            hardware_seed: Unique hardware identifier (e.g., TPM serial)
            manufacturer: Device manufacturer
            model: Device model

        Returns:
            DeviceIdentity with generated TPM keys

        Raises:
            ValueError: If device already registered
        """
        identity = generate_tpm_identity(hardware_seed)
        identity.manufacturer = manufacturer
        identity.model = model

        # Check for duplicate
        if identity.device_id in self._devices:
            raise ValueError(f"Device {identity.device_id} already registered")

        # Check TPM key collision
        if identity.tpm_pubkey in self._tpm_key_index:
            raise ValueError("TPM key collision detected")

        self._devices[identity.device_id] = identity
        self._tpm_key_index[identity.tpm_pubkey] = identity.device_id

        self._registration_log.append({
            "action": "register",
            "device_id": identity.device_id,
            "timestamp": time.time(),
            "hardware_serial": identity.hardware_serial,
        })

        return identity

    def get_device(self, device_id: str) -> Optional[DeviceIdentity]:
        """Get device identity by ID."""
        return self._devices.get(device_id)

    def verify_identity(self, device_id: str, tpm_pubkey: str) -> bool:
        """
        Verify a device's TPM public key matches registration.

        In production, this would verify against TPM certificate chain.
        """
        device = self._devices.get(device_id)
        if device is None:
            return False
        return device.tpm_pubkey == tpm_pubkey

    def update_status(self, device_id: str, status: DeviceStatus) -> bool:
        """Update device status."""
        device = self._devices.get(device_id)
        if device is None:
            return False
        device.status = status
        device.last_seen = time.time()
        return True

    def update_firmware(self, device_id: str, version: str) -> bool:
        """Update device firmware version."""
        device = self._devices.get(device_id)
        if device is None:
            return False
        device.firmware_version = version
        device.last_seen = time.time()
        return True

    def heartbeat(self, device_id: str) -> bool:
        """Record a heartbeat from a device."""
        device = self._devices.get(device_id)
        if device is None:
            return False
        device.last_seen = time.time()
        if device.status == DeviceStatus.OFFLINE:
            device.status = DeviceStatus.ONLINE
        return True

    def get_stale_devices(self, timeout_s: float = 300.0) -> List[DeviceIdentity]:
        """Get devices that haven't sent heartbeat within timeout."""
        now = time.time()
        return [d for d in self._devices.values()
                if (now - d.last_seen) > timeout_s]

    def list_devices(self, status: Optional[DeviceStatus] = None) -> List[DeviceIdentity]:
        """List all devices, optionally filtered by status."""
        if status is None:
            return list(self._devices.values())
        return [d for d in self._devices.values() if d.status == status]

    def to_dict(self) -> Dict[str, Any]:
        return {
            "device_count": self.device_count,
            "online_count": self.online_count,
            "devices": {did: d.to_dict() for did, d in self._devices.items()},
            "registration_log": self._registration_log[-100:],  # Last 100
        }


